    public boolean isValidSudoku(char[][] board) {
        Set<Integer> lineSet = new HashSet<>();
        Set<Integer> columnSet = new HashSet<>();
        Set<Integer> areaSet = new HashSet<>();
        for (int i = 0; i < 9; i++) {
            for (int j = 0; j < 9; j++) {
                if (board[i][j] == '.') {
                    continue;
                }
                int n = board[i][j] - '0';
                if (lineSet.contains(i * 9 + n)) {
                    return false;
                }
                if (columnSet.contains(j * 9 + n)) {
                    return false;
                }
                int areaIndex = (i / 3) * 3 + (j / 3);
                if (areaSet.contains(areaIndex * 9 + n)) {
                    return false;
                }
                lineSet.add(i * 9 + n);
                columnSet.add(j * 9 + n);
                areaSet.add(areaIndex * 9 + n);
            }
        }
        return true;
    }
